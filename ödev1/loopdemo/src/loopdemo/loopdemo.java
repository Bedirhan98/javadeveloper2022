package loopdemo;

public class loopdemo {

	public static void main(String[] args) {
		//LESSON 11
		
		for(int i=1;i<11;i++) {
			System.out.println(i);
		}
		System.out.println("END OF LOOP 1");

		for(int j=1;j<20;j+=2) {
			System.out.println(j);
		}
		System.out.println("END OF LOOP 2");
	}

}
